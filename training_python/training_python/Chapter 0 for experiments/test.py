    print(f"–†–æ–±–æ—Ç –±–µ—Ä–µ—Ç {cake} –∫–æ—Ä–∂...")
    print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: –¢–æ—Ä—Ç '{process_name}' –≥–æ—Ç–æ–≤! üéÇ")
